using System;

namespace CshBook.Lessons.Chapter0.Lesson02Conditionals
{
    #region Теория
    /*
        До этого момента каждая программа шла по одному и тому же пути.
        Но в реальной жизни программа должна уметь реагировать на разные данные по-разному.

        Для этого в C# используются условные операторы.
     */

    /*
        Базовая конструкция:

        if (условие)
        {
            код, если условие true
        }
        else
        {
            код, если условие false
        }

        Условие всегда должно быть типа bool.
        Например:
        age >= 18
        number % 2 == 0
        password == "12345"
     */

    /*
        Если вариантов больше двух, используют else if:

        if (score == 5)
        {
            ...
        }
        else if (score == 4)
        {
            ...
        }
        else
        {
            ...
        }

        Такая запись удобнее, чем вкладывать один if в другой.
     */

    /*
        Важно понимать разницу между двумя отдельными if и цепочкой if / else if:

        - два отдельных if могут выполниться оба;
        - в цепочке if / else if / else выполнится только одна ветка.

        Это одна из самых частых ошибок у новичков.
     */

    /*
        Тернарный оператор - короткая запись для простого выбора из двух значений.

        Структура:
        условие ? значениеЕслиTrue : значениеЕслиFalse

        Пример:
        string access = age >= 18 ? "Доступ открыт" : "Доступ запрещен";

        Его удобно использовать, когда нужно получить одно значение.
        Если логика длинная, обычный if читается лучше.
     */

    /*
        В условиях часто используют логические операторы:

        - && - "и"
        - || - "или"
        - !  - "не"

        Примеры:
        age >= 18 && hasTicket
        month == 12 || month == 1 || month == 2
        !isBlocked

        Они нужны, когда простого сравнения уже недостаточно
        и надо проверить сразу несколько правил.
     */

    /*
        Хорошая привычка:
        сначала продумать все варианты на обычном языке,
        а потом уже переводить их в if / else if / else.

        Например:
        - если сумма меньше 1000 -> скидки нет;
        - если сумма от 1000 до 2999 -> скидка 5%;
        - иначе -> скидка 10%.

        Так код получается чище, а вероятность запутаться в диапазонах меньше.
     */

    /*
        Когда использовать условия:

        - доступ разрешить или запретить;
        - найти большее число;
        - выбрать скидку;
        - проверить корректность ввода;
        - вывести разные сообщения в зависимости от ситуации.
     */
    #endregion

    internal static class Lesson02Conditionals
    {
        public static void Main_()
        {
            Console.Write("Введите возраст: ");
            int age = Convert.ToInt32(Console.ReadLine());

            if (age >= 18)
            {
                Console.WriteLine("Доступ открыт");
            }
            else
            {
                Console.WriteLine("Доступ запрещен");
            }

            Console.Write("Введите первое число: ");
            int a = Convert.ToInt32(Console.ReadLine());

            Console.Write("Введите второе число: ");
            int b = Convert.ToInt32(Console.ReadLine());

            if (a > b)
            {
                Console.WriteLine("Первое число больше второго");
            }
            else if (b > a)
            {
                Console.WriteLine("Второе число больше первого");
            }
            else
            {
                Console.WriteLine("Числа равны");
            }

            string parity = a % 2 == 0 ? "четное" : "нечетное";
            Console.WriteLine($"Первое число - {parity}");

            string login = "admin";
            string password = "12345";
            bool canEnter = login == "admin" && password == "12345";
            Console.WriteLine($"Логин и пароль верны: {canEnter}");
        }
    }

    #region Задачи
    /*
        Разминка

        1. Проверка возраста.
           Ввод: возраст.
           Вывод:
           - "Можно войти", если возраст 18 и больше;
           - "Пока нельзя", если меньше 18.

        2. Четное или нечетное.
           Ввод: целое число.
           Вывод: "Четное" или "Нечетное".
           Сделай обычным if, а потом тем же смыслом через тернарный оператор.

        3. Кто больше.
           Ввод: два числа.
           Вывод:
           - "Первое больше";
           - "Второе больше";
           - "Числа равны".

        Основные задачи

        4. Самое большое из трех.
           Ввод: три числа.
           Вывод: максимальное число.

        5. Скидка в магазине.
           Ввод: сумма покупки.
           Правила:
           - до 1000: скидки нет;
           - от 1000 до 2999: скидка 5%;
           - от 3000 и выше: скидка 10%.
           Вывод: размер скидки и итоговая сумма.

        6. Проверка пароля.
           Ввод: логин и пароль.
           Правильные значения задай прямо в коде, например:
           логин "admin", пароль "12345".
           Вывод:
           - "Вход выполнен";
           - "Неверный логин или пароль".

        7. Категория билета.
           Ввод: возраст.
           Вывод:
           - "Детский билет", если возраст меньше 14;
           - "Подростковый билет", если от 14 до 17;
           - "Взрослый билет", если 18 и больше.

        8. Номер месяца.
           Ввод: номер месяца от 1 до 12.
           Вывод:
           - "Зима", "Весна", "Лето" или "Осень".
           Если число вне диапазона 1..12, выведи "Такого месяца нет".

        Задачи на перенос

        9. Четырехзначное число.
           Ввод: целое число.
           Вывод:
           - "Успешно", если число четырехзначное;
           - "Неудача" в остальных случаях.
           Эту задачу реши именно через тернарный оператор.

        10. Мини-калькулятор.
            Ввод:
            - первое число;
            - второе число;
            - знак операции: +, -, * или /.
            Вывод: результат операции.
            Если введен неизвестный знак, выведи "Неизвестная операция".
            Если выбрано деление на 0, выведи понятное сообщение об ошибке.
     */
    #endregion
}
