using System;

namespace CshBook.Lessons.Chapter0.Lesson01DataTypes
{
    #region Теория
    /*
        Урок 1 посвящен самой базе: переменным, типам данных, вводу и выводу.

        После этого урока ты должен уметь:
        - хранить данные в переменных;
        - читать данные из консоли;
        - выполнять простые вычисления;
        - собирать понятный текстовый вывод.
     */

    /*
        Переменная - это именованное место в памяти, где хранится значение.

        Пример:
        int age = 18;

        Здесь:
        - int - тип данных;
        - age - имя переменной;
        - 18 - значение.

        C# - язык со статической типизацией. Это значит, что тип переменной известен заранее.
        Если переменная объявлена как int, строку в нее положить уже нельзя.
     */

    /*
        Основные типы, которые будут часто встречаться с самого начала:

        - int - целые числа: 1, 25, -7
        - double - дробные числа: 3.14, 0.5
        - string - текст: "Привет"
        - char - один символ: 'A'
        - bool - true или false

        Пока этого набора достаточно почти для всех первых программ.
        Остальные типы тоже существуют, но на старте они не нужны каждый день.
     */

    /*
        Полезные правила оформления:

        - строки пишутся в двойных кавычках: "text"
        - символ пишется в одинарных кавычках: 'A'
        - каждая инструкция заканчивается символом ;
        - код внутри фигурных скобок принято писать с отступами

        Имена переменных в C# обычно пишут так:
        string userName = "Anton";

        Это называется camelCase: первое слово с маленькой буквы, следующие - с большой.
     */

    /*
        С переменными можно выполнять операции.

        Для чисел:
        - + сложение
        - - вычитание
        - * умножение
        - / деление
        - % остаток от деления

        Пример:
        int fullBoxes = 17 / 5;   // 3
        int extraItems = 17 % 5;  // 2

        Для строк тоже работает +:
        string fullName = "Anton" + " " + "Aley";
     */

    /*
        Важно различать целочисленное и дробное деление.

        Если обе части выражения имеют тип int,
        результат тоже будет int, дробная часть отбросится:

        7 / 2   // 3

        Если хотя бы одно число дробное, результат будет дробным:

        7 / 2.0 // 3.5

        Это особенно важно в задачах на средний балл, цену, время и проценты.
     */

    /*
        C# умеет сравнивать значения.

        Результат сравнения - это bool:
        - true, если условие выполняется
        - false, если не выполняется

        Примеры:
        5 > 3      // true
        10 == 10   // true
        7 != 2     // true
        2 > 8      // false

        Такие выражения особенно важны, потому что на них потом строятся условия и циклы.
     */

    /*
        Ввод и вывод в консоль:

        - Console.WriteLine(...) выводит текст и переводит курсор на новую строку
        - Console.Write(...) выводит текст без перехода на новую строку
        - Console.ReadLine() читает строку, введенную пользователем

        Важно: ReadLine() всегда возвращает строку.
        Если тебе нужно число, строку надо преобразовать:

        string ageText = Console.ReadLine();
        int age = int.Parse(ageText);

        Если ввести не число, программа завершится с ошибкой.
        Пока это нормально: обработку ошибок мы разберем позже.
     */

    /*
        Для красивого вывода очень удобны интерполированные строки.

        Пример:
        string name = "Антон";
        int age = 18;
        Console.WriteLine($"Привет, {name}! Тебе {age} лет.");

        Знак $ перед строкой позволяет вставлять переменные прямо в текст через { }.
     */

    /*
        В интерполированной строке можно не только вставлять значение,
        но и управлять его форматом.

        Самые полезные варианты на старте:

        - {score:F1} - одно число после запятой
        - {score:F2} - два числа после запятой

        Пример:
        double average = 4.6666;
        Console.WriteLine($"Средний балл: {average:F1}"); // 4.7
        Console.WriteLine($"Средний балл: {average:F2}"); // 4.67

        Буква F означает fixed-point, то есть обычную запись числа
        с фиксированным количеством знаков после запятой.
     */
    #endregion

    internal static class Lesson01DataTypes
    {
        public static void Main_()
        {
            int age = 18;
            double height = 1.78;
            string name = "Anton";
            char group = 'A';
            bool likesCSharp = true;

            Console.WriteLine($"{name}, возраст: {age}, рост: {height}, группа: {group}, любит C#: {likesCSharp}");

            int apples = 17;
            int boxes = 5;

            Console.WriteLine($"Полных коробок: {apples / boxes}");
            Console.WriteLine($"Лишних яблок: {apples % boxes}");

            int firstNumber = 7;
            int secondNumber = 2;
            Console.WriteLine($"7 / 2 = {firstNumber / secondNumber}");
            Console.WriteLine($"7 / 2.0 = {firstNumber / 2.0}");

            string priceText = "199";
            int price = int.Parse(priceText);
            Console.WriteLine($"Цена после наценки 1 рубль: {price + 1}");

            double averageScore = 4.6666;
            Console.WriteLine($"Средний балл с одним знаком: {averageScore:F1}");
            Console.WriteLine($"Средний балл с двумя знаками: {averageScore:F2}");

            Console.Write("Введите имя: ");
            string userName = Console.ReadLine();

            Console.Write("Введите возраст: ");
            int userAge = int.Parse(Console.ReadLine());

            bool isAdult = userAge >= 18;

            Console.WriteLine($"Привет, {userName}! Тебе {userAge} лет. Совершеннолетие: {isAdult}");
        }
    }

    #region Задачи
    /*
        Разминка

        1. Мини-визитка.
           Ввод: имя, город, любимое занятие.
           Вывод: одна красивая строка через интерполяцию.
           Пример: "Меня зовут Антон, я живу в Москве и люблю писать код."

        2. Стоимость покупки.
           Ввод: цена одного товара и количество товаров.
           Вывод: общая стоимость покупки.

        3. Время в пути.
           Ввод: общее количество минут.
           Вывод: сколько это часов и сколько минут осталось.
           Подсказка: используй / и %.

        Основные задачи

        4. Мини-калькулятор.
           Ввод: два целых числа.
           Вывод:
           - сумма;
           - разность;
           - произведение;
           - результат целочисленного деления;
           - остаток от деления.

        5. Коробки и остаток.
           Ввод: сколько всего карандашей и сколько карандашей помещается в одну коробку.
           Вывод:
           - сколько полных коробок получится;
           - сколько карандашей останется.

        6. Проверка сравнений.
           Ввод: два числа.
           Вывод в отдельных строках:
           - первое число больше второго;
           - первое число равно второму;
           - первое число не равно второму.
           Результат должен выводиться как true или false.

        7. Карточка ученика.
           Ввод: имя, возраст, средний балл.
           Вывод: одна строка в формате
           "Ученик: ..., возраст: ..., средний балл: ..."

        Задача на перенос

        8. Чек в магазине.
           Ввод:
           - название товара;
           - цена за штуку;
           - количество.
           Вывод:
           - название товара;
           - количество;
           - итоговая сумма.
           Все данные выведи в 2-3 понятных строках, будто это мини-чек.

        9. Учебная статистика.
           Ввод: имя ученика и три оценки.
           Вывод:
           - сумма оценок;
           - средний балл;
           - красивая итоговая строка с именем и результатом.
     */
    #endregion
}
