using System;

namespace CshBook.Lessons.Chapter0.Lesson04WhileLoop
{
    #region Теория
    /*
        while нужен тогда, когда ты не знаешь заранее, сколько раз повторится код.

        Примеры:
        - читать числа, пока пользователь не введет 0;
        - спрашивать пароль, пока он не станет правильным;
        - выполнять действие, пока не достигнута цель.
     */

    /*
        Структура while:

        while (условие)
        {
            код
        }

        Пока условие true, цикл продолжается.
        Как только условие становится false, цикл останавливается.
     */

    /*
        Очень важно менять данные внутри цикла.

        Если условие никогда не станет false, получится бесконечный цикл.
        Это одна из самых частых ошибок.

        Пример:
        int i = 1;
        while (i <= 5)
        {
            Console.WriteLine(i);
            i++;
        }
     */

    /*
        Иногда цикл нужно остановить вручную.
        Для этого используют break.

        А если нужно пропустить одну итерацию и перейти к следующей - continue.
     */

    /*
        while особенно полезен в задачах с "сигналом завершения".

        Примеры:
        - читать числа, пока не введен 0;
        - спрашивать пароль, пока он неверный;
        - пополнять сумму, пока не достигнута цель.

        В таких задачах количество повторений заранее неизвестно,
        и именно поэтому while подходит лучше for.
     */

    /*
        do while похож на while, но сначала выполняет код, а потом проверяет условие.

        Структура:

        do
        {
            код
        }
        while (условие);

        Этот цикл удобен, когда тело должно выполниться хотя бы один раз.
        Например, когда мы обязаны хотя бы один раз спросить пароль или число.
     */

    /*
        В задачах с while часто используют:

        - счетчик - чтобы посчитать количество действий;
        - накопитель - чтобы собирать сумму;
        - текущее значение - чтобы понимать, когда остановиться.

        Пример мышления:
        "мне нужна сумма" -> завожу переменную sum;
        "мне нужно количество попыток" -> завожу переменную count.
     */
    #endregion

    internal class Lesson04WhileLoop
    {
        public static void Main_()
        {
            int i = 1;
            while (i <= 5)
            {
                Console.WriteLine(i);
                i++;
            }

            Console.WriteLine("----");

            int total = 0;
            int number = 1;

            while (number != 0)
            {
                Console.Write("Введите число (0 - закончить): ");
                number = Convert.ToInt32(Console.ReadLine());
                total += number;
            }

            Console.WriteLine($"Сумма введенных чисел: {total}");

            Console.WriteLine("----");

            string password;
            do
            {
                Console.Write("Введите пароль: ");
                password = Console.ReadLine();
            }
            while (password != "12345");

            Console.WriteLine("Пароль принят");
        }
    }

    #region Задачи
    /*
        Разминка

        1. Выведи числа от 1 до 10 с помощью while.

        2. Ввод: число n.
           Выведи все числа от 1 до n с помощью while.

        3. Ввод: число n.
           Найди сумму всех чисел от 1 до n с помощью while.

        Основные задачи

        4. Сумма до сигнала остановки.
           Пользователь вводит числа.
           Когда введено 0, программа завершается.
           Выведи сумму всех введенных чисел, не считая завершающий 0.

        5. Подсчет четных чисел.
           Пользователь вводит числа, пока не введет 0.
           Нужно посчитать, сколько среди них четных.

        6. Количество цифр.
           Ввод: целое положительное число.
           Вывод: сколько цифр в этом числе.
           Подсказка: можно делить число на 10, пока оно не станет 0.

        7. Цель накоплений.
           Ввод:
           - сумма-цель;
           - затем суммы пополнений по одной.
           Программа должна считать, сколько пополнений понадобилось,
           чтобы накопить не меньше нужной суммы.

        Задачи на do while

        8. Проверка пароля.
           Спрашивай пароль, пока пользователь не введет "12345".
           После успешного ввода выведи "Доступ открыт".

        9. Проверка положительного числа.
           Спрашивай число до тех пор, пока пользователь не введет число больше 0.
           После этого выведи "Число принято".

        10. Мини-анкета.
            Спрашивай у пользователя, хочет ли он продолжить заполнение анкеты.
            Пока ответ "да", программа продолжает работу.
            Как только пользователь введет "нет", цикл заканчивается.
            Внутри цикла можно просто выводить строку "Заполняем следующий блок анкеты...".
     */
    #endregion
}
