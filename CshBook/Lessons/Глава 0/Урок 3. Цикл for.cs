using System;

namespace CshBook.Lessons.Chapter0.Lesson03ForLoop
{
    #region Теория
    /*
        Цикл нужен, когда один и тот же код нужно выполнить несколько раз.

        Цикл for особенно удобен тогда, когда ты заранее понимаешь:
        - с какого значения начинаешь;
        - до какого значения идешь;
        - как меняется счетчик на каждой итерации.
     */

    /*
        Базовая структура:

        for (начало; условие; шаг)
        {
            код
        }

        Пример:
        for (int i = 0; i < 5; i++)
        {
            Console.WriteLine(i);
        }

        Здесь:
        - int i = 0 - начальное значение;
        - i < 5 - пока условие true, цикл работает;
        - i++ - после каждой итерации i увеличивается на 1.
     */

    /*
        Очень важно следить за границами.

        Частые ошибки:
        - случайно вывести на одно число меньше;
        - случайно вывести на одно число больше;
        - забыть изменить счетчик;
        - выбрать неправильное условие.

        Например, i <= 5 и i < 5 - это разные вещи.
     */

    /*
        Шаг цикла можно менять.

        Примеры:
        i += 2  // идти по 2
        i--     // идти назад

        Благодаря этому for подходит не только для "1, 2, 3...",
        но и для обратного отсчета, таблиц, шагов, фильтрации и шаблонов вывода.
     */

    /*
        У цикла for есть несколько очень частых сценариев:

        - просто пройти диапазон чисел;
        - накопить сумму;
        - отфильтровать только подходящие значения;
        - построить таблицу или текстовую фигуру.

        Если видишь задачу "от 1 до n", "для каждого числа", "ровно 10 раз",
        скорее всего for подходит лучше всего.
     */

    /*
        Вложенный цикл - это цикл внутри цикла.

        Он нужен, когда одна повторяющаяся задача содержит другую.
        Чаще всего это:
        - таблицы;
        - прямоугольники и другие текстовые фигуры;
        - работа со строками и столбцами.
     */

    /*
        Полезная привычка при работе с for:
        мысленно прогонять первые 2-3 итерации руками.

        Например, если цикл идет от 1 до 5, попробуй на бумаге ответить:
        - какое значение у счетчика на старте;
        - что выполнится на первой итерации;
        - когда цикл остановится.

        Такой "сухой прогон" очень хорошо ловит ошибки на границах.
     */
    #endregion

    internal class Lesson03ForLoop
    {
        public static void Main_()
        {
            for (int i = 1; i <= 5; i++)
            {
                Console.WriteLine(i);
            }

            Console.WriteLine("----");

            for (int i = 0; i <= 10; i += 2)
            {
                Console.WriteLine(i);
            }

            Console.WriteLine("----");

            int sum = 0;
            for (int i = 1; i <= 5; i++)
            {
                sum += i;
            }
            Console.WriteLine($"Сумма чисел от 1 до 5 = {sum}");

            Console.WriteLine("----");

            Console.WriteLine("Числа от 1 до 15, которые делятся на 3:");
            for (int i = 1; i <= 15; i++)
            {
                if (i % 3 == 0)
                {
                    Console.WriteLine(i);
                }
            }

            Console.WriteLine("----");

            for (int row = 0; row < 3; row++)
            {
                for (int column = 0; column < 5; column++)
                {
                    Console.Write("*");
                }
                Console.WriteLine();
            }
        }
    }

    #region Задачи
    /*
        Разминка

        1. Приветствие по расписанию.
           Выведи фразу "You are welcome!" ровно 10 раз.

        2. Повтор фразы.
           Ввод: число n.
           Вывод: фраза "Silence is golden" должна появиться n раз.

        3. Прямой и обратный счет.
           Ввод: число n.
           Вывод:
           - все числа от 1 до n;
           - затем все числа от n до 1.

        Основные задачи

        4. Сумма диапазона.
           Ввод: число n.
           Вывод: сумма всех чисел от 1 до n.

        5. Только нужные числа.
           Выведи все числа от 1 до 100, которые делятся на 3 или на 5.

        6. Таблица для одного числа.
           Ввод: число n.
           Вывод: таблица умножения для этого числа от 1 до 10.
           Например:
           7 * 1 = 7
           7 * 2 = 14
           ...

        7. Степени двойки.
           Ввод: число n.
           Выведи все степени двойки, которые не превышают n.
           Пример для 20: 1 2 4 8 16

        Задачи на перенос

        8. Прямоугольник из символов.
           Ввод: высота h и ширина w.
           Вывод: прямоугольник из символов # размером h на w.

        9. Рамка.
           Ввод: число n.
           Вывод: квадрат n на n, у которого граница состоит из *,
           а внутри стоят пробелы.

        10. Ряды в кинотеатре.
            Ввод: количество рядов и количество мест в ряду.
            Вывод:
            Ряд 1: 1 2 3 4 5
            Ряд 2: 1 2 3 4 5
            ...
            Эта задача полезна, чтобы почувствовать вложенные циклы в прикладном сценарии.
     */
    #endregion
}
