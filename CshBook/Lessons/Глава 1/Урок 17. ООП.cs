namespace CshBook.Lessons.Chapter1.Lesson17OopBasics
{
    #region Теория
    /*
        ООП - это объектно-ориентированное программирование.

        Главная идея:
        программу можно строить не только из отдельных переменных и методов,
        а из объектов.

        Объект объединяет данные и действия.
     */

    /*
        Например, если мы пишем мини-игру про питомца,
        у питомца есть данные:

        - имя;
        - возраст;
        - настроение;
        - количество игр.

        И есть действия:

        - играть;
        - взрослеть;
        - выводить информацию о себе.
     */

    /*
        Без класса данные быстро расползаются:

        string petName = "Бобик";
        int petAge = 5;
        int petMood = 80;

        Если питомцев станет два или десять,
        переменных станет слишком много.

        Класс решает эту проблему:
        мы один раз описываем, что такое питомец,
        а потом создаем столько питомцев, сколько нужно.
     */

    /*
        Класс - это чертеж.
        Объект - это конкретная вещь, созданная по этому чертежу.

        Pet - класс.
        bobik и sharik - объекты класса Pet.
     */

    /*
        Внутри класса могут быть:

        - поля: данные объекта;
        - методы: действия объекта.

        На этом уроке используем простые public-поля.
        Позже отдельно разберем, почему поля часто закрывают
        и используют свойства.
     */

    /*
        Важно:
        каждый объект хранит свои значения полей.

        Если у bobik возраст 5,
        а у sharik возраст 2,
        изменение одного объекта не меняет поля другого объекта.
     */
    #endregion

    class Pet
    {
        public string Name = "";
        public int Age;
        public int Mood = 50;
        public int PlayCount;

        public void Play()
        {
            PlayCount++;
            Mood += 10;

            Console.WriteLine($"{Name} играет. Настроение: {Mood}. Игр: {PlayCount}");
        }

        public void GrowOlder()
        {
            Age++;
            Console.WriteLine($"{Name} стал старше. Возраст: {Age}");
        }

        public void PrintInfo()
        {
            Console.WriteLine($"Питомец: {Name}, возраст: {Age}, настроение: {Mood}");
        }
    }

    internal static class Lesson17OopBasics
    {
        public static void Main_()
        {
            Pet bobik = new Pet();
            bobik.Name = "Бобик";
            bobik.Age = 5;

            Pet sharik = new Pet();
            sharik.Name = "Шарик";
            sharik.Age = 2;

            bobik.PrintInfo();
            sharik.PrintInfo();

            Console.WriteLine("----");

            bobik.Play();
            bobik.Play();
            sharik.Play();

            Console.WriteLine("----");

            bobik.GrowOlder();

            Console.WriteLine("----");

            bobik.PrintInfo();
            sharik.PrintInfo();
        }
    }

    #region Задачи
    /*
        Разминка

        1. Класс питомца.
           Создай класс Pet с полями Name, Age и Mood.
           Создай два объекта Pet и заполни их поля разными значениями.

        2. Вывод информации.
           Добавь в Pet метод PrintInfo(),
           который выводит имя, возраст и настроение питомца.

        3. Метод игры.
           Добавь метод Play(),
           который увеличивает настроение питомца на 10.

        Основные задачи

        4. Счетчик игр.
           Добавь поле PlayCount.
           Каждый вызов Play() должен увеличивать PlayCount на 1.

        5. Старение после игр.
           После каждой десятой игры возраст питомца должен увеличиваться на 1.

        6. Метод взросления.
           Добавь метод GrowOlder(),
           который увеличивает возраст питомца на 1.

        7. Класс игрока.
           Создай класс Player с полями Name, Level и Coins.
           Добавь методы AddCoins(int count) и PrintInfo().

        Задачи на перенос

        8. Мини-инвентарь.
           Создай класс InventoryItem с полями Title, Count и Price.
           Добавь метод PrintInfo().

        9. Тренировка персонажа.
           Создай класс Hero с полями Name, Strength и TrainingCount.
           Метод Train() должен увеличивать Strength и TrainingCount.

        10. Творческая задача.
            Создай свой класс для простой игры или приложения.
            У класса должно быть минимум три поля и два метода.
     */
    #endregion
}
