    /* Массивы (Arrays)
    В C# массивы представляют собой структуру данных, которая позволяет хранить коллекцию элементов одного типа. 
    Массивы имеют фиксированный размер, который определяется при их создании и не может быть изменен. 
    Доступ к элементам массива осуществляется по их индексу, который начинается с 0.

    Массивы могут быть одномерными, многомерными и ступенчатыми (массивы массивов).


    В массиве всегда должен быть один тип данных. однако, можно использовать несколько, используя object[].
    */

    internal class SixthLesson
