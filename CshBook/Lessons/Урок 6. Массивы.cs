        public static void Main()
        {
            // Одномерный массив
            int[] numbers = new int[5];  // Массив из 5 элементов типа int
            numbers[0] = 10;
            numbers[1] = 20;
            numbers[2] = 30;
            numbers[3] = 40;
            numbers[4] = 50;

            int[] numbers2 = new int[] {1,2,3,4,5}; // можно задавать и так


            object[] objects = new object[] { 1, "Hello", 3.14, new object() }; // проблем не будет 
            objects[0] = "Hello"; // можно спокойно менять 

            for (int i = 0; i < objects.Length; i++)
            {
                Console.WriteLine(objects[i]);
            }



            // Вывод элементов массива

            for (int i = 0; i < numbers.Length; i++)
            {
                Console.WriteLine(numbers[i]);
            }

            // или 

            foreach (int number in numbers)
            {
                Console.WriteLine(number);
            }

            // Многомерный массив (матрица)
            int[,] matrix = new int[3, 3];  // Матрица 3x3
            matrix[0, 0] = 1;
            matrix[0, 1] = 2;
            matrix[0, 2] = 3;
            matrix[1, 0] = 4;
            matrix[1, 1] = 5;
            matrix[1, 2] = 6;
            matrix[2, 0] = 7;
            matrix[2, 1] = 8;
            matrix[2, 2] = 9;

            // Вывод элементов матрицы
            for (int i = 0; i < 3; i++)
            {
                for (int j = 0; j < 3; j++)
                {
                    Console.Write(matrix[i, j] + " ");
                }
                Console.WriteLine();
            }

            // Ступенчатый массив (массив массивов)
            int[][] jaggedArray = new int[3][];
            jaggedArray[0] = new int[] { 1, 2, 3 };
            jaggedArray[1] = new int[] { 4, 5 };
            jaggedArray[2] = new int[] { 6, 7, 8, 9 };

            // Вывод элементов ступенчатого массива
            for (int i = 0; i < jaggedArray.Length; i++)
            {
                for (int j = 0; j < jaggedArray[i].Length; j++)
                {
                    Console.Write(jaggedArray[i][j] + " ");
                }
                Console.WriteLine();
            }

            // Основные операции с массивами
            int[] array = { 1, 2, 3, 4, 5 };

            // Длина массива
            Console.WriteLine("Длина массива: " + array.Length);

            // Сумма элементов массива
            int sum = array.Sum();
            Console.WriteLine("Сумма элементов: " + sum);

            // Максимальный элемент
            int max = array.Max();
            Console.WriteLine("Максимальный элемент: " + max);

            // Минимальный элемент
            int min = array.Min();
            Console.WriteLine("Минимальный элемент: " + min);

            // Сортировка массива
            Array.Sort(array);
            Console.WriteLine("Отсортированный массив: " + string.Join(", ", array));

            // Обратный порядок
            Array.Reverse(array);
            Console.WriteLine("Массив в обратном порядке: " + string.Join(", ", array));

            // Поиск элемента
            int index = Array.IndexOf(array, 3);
            Console.WriteLine("Индекс элемента 3: " + index);

            // Заполнение массива
            Array.Fill(array, 0);
            Console.WriteLine("Заполненный массив: " + string.Join(", ", array));

            // Копирование массива
            int[] copyArray = new int[array.Length];
            Array.Copy(array, copyArray, array.Length);
            Console.WriteLine("Копия массива: " + string.Join(", ", copyArray));

            // Очистка массива
            Array.Clear(array, 0, array.Length);
            Console.WriteLine("Очищенный массив: " + string.Join(", ", array));
        }

