using System;

namespace CshBook.Lessons.Chapter0.Lesson08Methods
{
    #region Теория
    /*
        Метод нужен, чтобы вынести повторяющийся или логически отдельный код в одно место.

        Если одна и та же логика нужна несколько раз, не стоит копировать ее целиком.
        Лучше оформить ее как метод и вызывать по имени.
     */

    /*
        Зачем это нужно:

        - код становится короче;
        - программа лучше читается;
        - ошибки легче исправлять;
        - одну и ту же логику можно использовать повторно.

        Это один из первых шагов к хорошей декомпозиции.
     */

    /*
        У метода может быть:

        - имя;
        - параметры;
        - возвращаемое значение.

        Пример:
        public static int Add(int a, int b)
        {
            return a + b;
        }

        Здесь:
        - int после static - тип результата;
        - Add - имя метода;
        - int a, int b - параметры.
     */

    /*
        Важно различать:

        - объявление метода - когда мы его описываем;
        - вызов метода - когда мы его используем.

        Пример:
        int result = Add(2, 3);

        Здесь Add(2, 3) - это вызов.
        Числа 2 и 3 называются аргументами.
     */

    /*
        Если метод ничего не возвращает, используют void.

        Пример:
        public static void PrintGreeting(string name)
        {
            Console.WriteLine($"Привет, {name}!");
        }

        Такой метод что-то делает, но не отдает результат обратно.
     */

    /*
        Метод с return обычно отвечает на вопрос:
        "какое значение нужно получить?"

        Метод с void чаще отвечает на вопрос:
        "какое действие нужно выполнить?"

        Примеры:
        - Add(...) -> вернуть сумму
        - IsEven(...) -> вернуть true или false
        - PrintGreeting(...) -> просто вывести текст
     */

    /*
        Когда использовать метод:

        - когда видишь повторяющийся код;
        - когда хочешь назвать часть логики понятным именем;
        - когда большую задачу удобно разбить на маленькие шаги.

        На старте важно научиться писать простые методы:
        - печать данных;
        - вычисление значения;
        - проверка условия.
     */

    /*
        Хорошая декомпозиция - это когда большая задача разбивается на шаги.

        Например, мини-чек можно мыслить так:
        - прочитать цену;
        - прочитать количество;
        - посчитать итог;
        - вывести результат.

        Каждый такой шаг можно оформить отдельным методом.
        Тогда программа читается сверху вниз как план действий.
     */
    #endregion

    internal static class Lesson08Methods
    {
        public static void PrintGreeting(string name)
        {
            Console.WriteLine($"Привет, {name}!");
        }

        public static int Factorial(int value)
        {
            int result = 1;

            for (int i = 1; i <= value; i++)
            {
                result *= i;
            }

            return result;
        }

        public static bool IsEven(int value)
        {
            return value % 2 == 0;
        }

        public static int SumFromOneTo(int value)
        {
            int sum = 0;

            for (int i = 1; i <= value; i++)
            {
                sum += i;
            }

            return sum;
        }

        public static void Main_()
        {
            Console.Write("Введите имя: ");
            string name = Console.ReadLine();
            PrintGreeting(name);

            Console.Write("Введите число: ");
            int number = int.Parse(Console.ReadLine());

            Console.WriteLine($"Факториал: {Factorial(number)}");
            Console.WriteLine($"Сумма от 1 до числа: {SumFromOneTo(number)}");
            Console.WriteLine($"Четное число: {IsEven(number)}");
        }
    }

    #region Задачи
    /*
        Разминка

