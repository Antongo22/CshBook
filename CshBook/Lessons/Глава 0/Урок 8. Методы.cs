        9. Последняя цифра числа.
           Напиши метод GetLastDigit(int number), который возвращает последнюю цифру числа.

        Задачи на декомпозицию

        10. Чек в магазине.
            Разбей программу на методы:
            - ReadPrice()
            - ReadQuantity()
            - CalculateTotal(int price, int quantity)
            - PrintReceipt(...)
            Программа должна считать стоимость покупки и печатать мини-чек.

        11. Карточка пользователя.
            Разбей программу на методы:
            - ReadName()
            - ReadAge()
            - FormatProfile(string name, int age)
            Итог: программа читает данные и выводит красивую строку о пользователе.

        12. Необязательная задача.
            Напиши метод ReverseString(string text), который возвращает строку в обратном порядке.
            Здесь уже придется объединить строки, цикл и методы.
     */
    #endregion
}
