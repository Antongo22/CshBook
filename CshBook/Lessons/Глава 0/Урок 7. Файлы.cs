using System;
using System.IO;

namespace CshBook.Lessons.Chapter0.Lesson07Files
{
    #region Теория
    /*
        До этого момента все данные жили только во время работы программы.
        Как только программа заканчивалась, данные исчезали.

        Файлы нужны для того, чтобы сохранять информацию надолго:
        - заметки;
        - настройки;
        - результаты;
        - простые отчеты.
     */

    /*
        На старте тебе не нужен FileStream и работа с байтами.
        Для большинства первых задач хватает класса File.

        Самые полезные методы:
        - File.WriteAllText(path, text) - записать текст в файл
        - File.ReadAllText(path) - прочитать весь текст файла
        - File.AppendAllText(path, text) - дописать текст в конец файла
        - File.Exists(path) - проверить, существует ли файл
        - File.ReadAllLines(path) - прочитать файл построчно
        - File.Copy(source, destination, true) - скопировать файл
     */

    /*
        Пример:

        string path = "notes.txt";
        File.WriteAllText(path, "Первая заметка");

        string text = File.ReadAllText(path);
        Console.WriteLine(text);

        Здесь notes.txt будет создан рядом с программой.
     */

    /*
        Когда путь простой, например "notes.txt",
        файл создается относительно папки, где запущена программа.

        На старте этого понимания достаточно:
        - "notes.txt" - файл рядом с программой;
        - "data/report.txt" - файл внутри папки data.

        Сложные абсолютные пути пока не нужны.
     */

    /*
        Важно:

        - путь к файлу - это строка;
        - если файла нет, а ты пытаешься читать его, будет ошибка;
        - если ты используешь WriteAllText, старое содержимое перезапишется;
        - если используешь AppendAllText, новый текст добавится в конец.

        Пока что лучше строить задачи так, чтобы файл создавался самой программой,
        а не требовал сложной ручной подготовки.
     */

    /*
        Если надо записать текст в несколько строк,
        можно использовать:

        - \n
        - Environment.NewLine

        Второй вариант чуть аккуратнее и лучше показывает,
        что речь именно о переносе строки.
     */

    /*
        File.ReadAllLines(path) возвращает массив строк.

        Это удобно, когда файл хранит данные построчно:
        - список покупок;
        - оценки;
        - числа для подсчета суммы.

        Если строка должна стать числом,
        ее все равно нужно преобразовать через int.Parse(...).
     */

    /*
        Главная цель урока:
        научиться сохранять текстовые данные в файл и читать их обратно.
     */
    #endregion

    internal class Lesson07Files
    {
        public static void Main_()
        {
            string notesPath = "notes.txt";

            File.WriteAllText(notesPath, "Первая строка" + Environment.NewLine + "Вторая строка");
            Console.WriteLine("Файл создан.");

            string text = File.ReadAllText(notesPath);
            Console.WriteLine("Содержимое файла:");
            Console.WriteLine(text);

            File.AppendAllText(notesPath, Environment.NewLine + "Третья строка");
            Console.WriteLine("После добавления строки:");
            Console.WriteLine(File.ReadAllText(notesPath));

            Console.WriteLine("----");

            if (File.Exists(notesPath))
            {
                string[] lines = File.ReadAllLines(notesPath);
                Console.WriteLine($"Количество строк: {lines.Length}");
            }

            Console.WriteLine("----");

            string backupPath = "notes_backup.txt";
            File.Copy(notesPath, backupPath, true);
            Console.WriteLine($"Создана копия файла: {backupPath}");
        }
    }

    #region Задачи
    /*
        Разминка

        1. Первая заметка.
           Создай файл note.txt и запиши в него строку:
           "Это моя первая заметка в файле."

        2. Чтение заметки.
           Прочитай содержимое файла note.txt и выведи его на экран.

        3. Добавление строки.
           Добавь в файл note.txt еще одну строку:
           "Я учусь работать с файлами."

        Основные задачи

        4. Мини-дневник.
           Запроси у пользователя одну строку и сохрани ее в diary.txt.
           После этого прочитай файл и выведи его содержимое.

        5. Список покупок.
           Создай файл shopping.txt и запиши в него 3-5 покупок,
           каждую с новой строки.
           Затем выведи количество строк в этом файле.

        6. Копия файла.
           Создай файл report.txt с любым текстом.
           Затем сделай его копию с именем report_copy.txt.

        7. Проверка существования файла.
           Проверь, существует ли файл notes.txt.
           Если существует, выведи "Файл найден".
           Если нет - "Файл не найден".

        8. История сообщений.
           Пользователь вводит две строки.
           Сохрани первую строку в chat.txt через WriteAllText,
           а вторую добавь через AppendAllText.
           Затем прочитай весь файл и выведи его.

        Задачи на перенос

        9. Мини-чек.
           Запроси у пользователя:
           - название товара;
           - цену;
           - количество.
           Сохрани результат в файл check.txt в удобном текстовом виде.
           Потом прочитай этот файл и выведи его на экран.

        10. Таблица результатов.
            Создай файл scores.txt, где в каждой строке лежит число.
            Прочитай все строки, преобразуй их в числа и найди сумму всех результатов.
            Подсказка: используй File.ReadAllLines.

        11. Необязательная задача.
            Сделай программу, которая ведет простой блокнот:
            - если файла еще нет, он создается;
            - новая заметка всегда добавляется в конец;
            - после добавления программа выводит весь текущий блокнот.
     */
    #endregion
}
