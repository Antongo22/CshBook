        3. Печать массива.
           Напиши метод PrintArray(int[] values), который выводит все элементы массива в одну строку.

        Основные задачи

        4. Сумма двух чисел.
           Напиши метод Add(int a, int b), который возвращает сумму двух чисел.

        5. Максимум из двух чисел.
           Напиши метод GetMax(int a, int b), который возвращает большее число.

        6. Проверка четности.
           Напиши метод IsEven(int number), который возвращает true, если число четное, и false в остальных случаях.

        7. Количество положительных чисел.
           Напиши метод CountPositive(int[] values), который возвращает количество положительных элементов массива.

        8. Среднее арифметическое.
