using System;

namespace CshBook.Lessons.Chapter0.Lesson06Arrays
{
    #region Теория
    /*
        Массив нужен, когда надо хранить несколько значений одного типа под одним именем.

        Пример:
        int[] grades = { 5, 4, 5, 3 };

        Здесь grades - это не одно число, а сразу набор чисел.
     */

    /*
        Важные свойства массива:

        - все элементы одного типа;
        - размер задается при создании;
        - доступ идет по индексу;
        - индексация начинается с 0.

        Пример:
        string[] names = { "Анна", "Борис", "Вика" };

        names[0] -> "Анна"
        names[1] -> "Борис"
        names[2] -> "Вика"
     */

    /*
        Создать массив можно двумя основными способами:

        int[] numbers = new int[5];
        numbers[0] = 10;
        numbers[1] = 20;

        или сразу с данными:

        int[] numbers = { 10, 20, 30, 40, 50 };
     */

    /*
        Чаще всего массив обрабатывают в цикле.

        Для этого удобно использовать:
        - for, когда нужен индекс;
        - foreach, когда нужно просто пройтись по всем элементам.

        Свойство Length показывает длину массива.
     */

    /*
        Самые частые задачи на массивы на старте:

        - пройти по всем элементам;
        - найти сумму;
        - найти максимум и минимум;
        - посчитать элементы по условию;
        - найти индекс нужного элемента.

        Это базовые шаблоны, которые потом будут встречаться постоянно.
     */

    /*
        Очень частая ошибка - выйти за границы массива.

        Если длина массива равна 5, индексы будут такими:
        0, 1, 2, 3, 4

        Попытка обратиться к numbers[5] уже приведет к ошибке.
        Поэтому в цикле обычно пишут:
        i < numbers.Length
        а не i <= numbers.Length
     */

    /*
        На старте самое важное:
        - научиться читать элементы по индексу;
        - проходить массив циклом;
        - находить сумму, максимум, минимум;
        - считать элементы по условию.

        Двумерные и ступенчатые массивы тоже существуют, но сначала важно освоить обычный одномерный массив.
     */

    /*
        Для двумерных массивов есть метод GetLength(...):

        - GetLength(0) - количество строк
        - GetLength(1) - количество столбцов

        Для ступенчатого массива длина может быть разной у каждой строки:
        weeklySales[day].Length

        Это важно, потому что не все таблицы обязаны быть "ровными".
     */

    /*
        У массива есть и готовые помощники:

        - Array.Copy(...) - скопировать элементы
        - Array.Reverse(...) - развернуть массив

        Их полезно знать, но сначала все равно важно уметь делать
        базовые задачи обычным циклом.
     */
    #endregion

    internal class Lesson06Arrays
    {
        public static void Main_()
        {
            int[] numbers = { 10, 20, 30, 40, 50 };

            Console.WriteLine("Элементы массива:");
            for (int i = 0; i < numbers.Length; i++)
            {
                Console.WriteLine($"numbers[{i}] = {numbers[i]}");
            }

            Console.WriteLine("----");

            int sum = 0;
            int max = numbers[0];
            int min = numbers[0];

            for (int i = 0; i < numbers.Length; i++)
            {
                sum += numbers[i];

                if (numbers[i] > max)
                {
                    max = numbers[i];
                }

                if (numbers[i] < min)
                {
                    min = numbers[i];
                }
            }

            Console.WriteLine($"Сумма: {sum}");
            Console.WriteLine($"Максимум: {max}");
            Console.WriteLine($"Минимум: {min}");
            Console.WriteLine($"Среднее: {(double)sum / numbers.Length:F2}");

            Console.WriteLine("----");

            int[] copy = new int[numbers.Length];
            Array.Copy(numbers, copy, numbers.Length);
            Array.Reverse(copy);
            Console.WriteLine("Массив в обратном порядке: " + string.Join(", ", copy));

            Console.WriteLine("----");

            int[,] matrix =
            {
                { 1, 2, 3 },
                { 4, 5, 6 }
            };

            Console.WriteLine("Двумерный массив:");
            for (int row = 0; row < matrix.GetLength(0); row++)
            {
                for (int column = 0; column < matrix.GetLength(1); column++)
                {
                    Console.Write(matrix[row, column] + " ");
                }
                Console.WriteLine();
            }

            Console.WriteLine("----");

            int[][] weeklySales =
            {
                new int[] { 3, 5, 2 },
                new int[] { 4, 1 },
                new int[] { 6, 2, 2, 1 }
            };

            Console.WriteLine("Ступенчатый массив:");
            for (int day = 0; day < weeklySales.Length; day++)
            {
                Console.Write($"День {day + 1}: ");
                for (int sale = 0; sale < weeklySales[day].Length; sale++)
                {
                    Console.Write(weeklySales[day][sale] + " ");
                }
                Console.WriteLine();
            }
        }
    }

    #region Задачи
    /*
        Разминка

        1. Температура за 5 дней.
           Сохрани в массив температуры за 5 дней и выведи их на экран.

        2. Сумма элементов.
           Дан массив из 6 целых чисел.
           Найди сумму всех элементов без использования LINQ.

        3. Максимум и минимум.
           Дан массив чисел.
           Найди максимальный и минимальный элемент.

        Основные задачи

        4. Количество четных чисел.
           Дан массив целых чисел.
           Посчитай, сколько в нем четных элементов.

        5. Поиск по значению.
           Дан массив и число, введенное пользователем.
           Найди индекс первого вхождения этого числа.
           Если числа нет, выведи -1.

        6. Оценки ученика.
           Дан массив оценок.
           Выведи:
           - все оценки;
           - средний балл;
           - количество пятерок;
           - лучшую оценку.

        7. Продажи за неделю.
           Дан массив из 7 чисел - продажи по дням.
           Найди:
           - общую сумму;
           - день с максимальной продажей;
           - количество дней, когда продаж было больше 10.

        8. Массив наоборот.
           Дан массив.
           Выведи его элементы в обратном порядке.
           Сначала реши без создания нового массива, просто при выводе.

        Задачи на двумерные массивы

        9. Таблица 3 на 3.
           Создай двумерный массив 3x3 и заполни его числами.
           Выведи:
           - всю таблицу;
           - сумму всех элементов;
           - сумму главной диагонали.

        10. Места в классе.
            Пусть 0 означает свободное место, а 1 - занятое.
            Дан двумерный массив мест.
            Найди:
            - сколько всего свободных мест;
            - в какой строке свободных мест больше всего.

        Задача на перенос

        11. Мини-склад.
            Есть массив с количеством товара по коробкам.
            Нужно вывести:
            - сколько коробок всего;
            - сколько товара всего;
            - в какой коробке больше всего товара;
            - есть ли пустые коробки.

        12. Необязательная задача.
            Создай ступенчатый массив, где каждая строка хранит покупки одного клиента.
            Выведи количество покупок у каждого клиента и общую сумму всех покупок.
     */
    #endregion
}
